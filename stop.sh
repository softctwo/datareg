#!/bin/bash
# 系统停止脚本
# 作者：张彦龙

echo "========================================"
echo "停止银行重要数据跨境数据管控系统"
echo "========================================"
echo ""

# 停止后端服务
echo "停止后端服务..."
pkill -f "uvicorn app.main:app"
if [ $? -eq 0 ]; then
    echo "✅ 后端服务已停止"
else
    echo "⚠️  后端服务可能未运行"
fi

# 停止前端服务
echo "停止前端服务..."
pkill -f "vite"
if [ $? -eq 0 ]; then
    echo "✅ 前端服务已停止"
else
    echo "⚠️  前端服务可能未运行"
fi

echo ""
echo "========================================"
echo "✅ 系统已停止"
echo "========================================"

